function viewRacquet() {
    let x = document.getElementById('rackquet-2');
    let y = document.getElementById('view-button-racq');
    if(x.style.display === "none") {
        x.style.display = "flex";
        y.innerHTML = "View Less";
    } else {
        x.style.display = "none";
        y.innerHTML = "View More";
    }
}

